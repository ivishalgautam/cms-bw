const { default: mongoose } = require("mongoose");

const ProjectManager = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model("ProjectManager", ProjectManager);
